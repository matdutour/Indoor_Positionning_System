package ips;

/**
 *
 * @author Mat
 */
public class Tag {
     private int x=0;
     private int y=0;
     
     public Tag(int x, int y)
     {
          this.x=x;
          this.y=y;
     }
     
     public int getX()
     {
          return x;
     }
     
     public int getY()
     {
          return y;
     }
}
